<table><tr><td>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
&nbsp;</td><td><img src="images/spitfire01.gif">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
<b><font face="Arial" size="3" color="#2A1FFF">Stability Concepts</font></b>
<br><br><font face="Arial" size="3" color="#2A1FFF">
The aircraft's response to momentary disturbance is associated with its <br>
inherent degree of stability built in by the designer, in each of the 
three axes, <br>
and occurring without any reaction from the pilot. <br><br>
There is another condition affecting flight, which is the aircraft's 
state of trim <br>
or equilibrium (where the net sum of all forces equals zero). <br>
Some aircraft can be trimmed by the pilot to fly 'hands off' for 
straight and <br>
level flight, for climb or for descent. 
<br><br>
Free flight models generally have to rely on the state of trim built in 
by the<br>
designer and adjusted by the rigger, while the remote controlled models 
have<br>
some form of trim devices which are adjustable during the flight.
<br><br>
An aircraft's stability is expressed in relation to each axis: <br>
<b>lateral stability</b> (stability in roll), <b>directional stability</b>
 (stability in yaw)<br>
and <b>longitudinal stability </b>(stability in pitch).<br>
Lateral and directional stabilities are inter-dependent.
<br><br>
<img src="images/spitfire_axis.gif" border="0" height="175" width="338">
<br><br>
Stability may be defined as follows:<br>
- Positive stability: tends to return to original condition after a 
disturbance.<br> 
- Negative stability: tends to increase the disturbance.<br> 
- Neutral stability: remains at the new condition.<br><br>
- <b>Static stability</b>: refers to the aircraft's <b>initial</b> 

response to a disturbance.<br>
A statically unstable aircraft will uniformly depart from a condition of
 equilibrium.<br><br> 
- <b>Dynamic stability</b>: refers to the aircraft's ability to damp out
 oscillations, which<br>
&nbsp;&nbsp;depends on how fast or how slow it responds to a 
disturbance.<br>
A dynamically unstable aircraft will (after a disturbance) start 
oscillating with<br>
increasing amplitude.<br>
A dynamically neutrally stable aircraft will continue oscillating after a
 disturbance<br>
but the amplitude of the oscillations will not change.

<br><br>
So, a statically stable aircraft may be dynamically unstable.<br>
Dynamic instability may be prevented by an even distribution of weight 
inside the<br> 
fuselage, avoiding too much weight concentration at the extremities or 
at the CG.<br>  
Also, control surfaces' max throws may affect the flight stability, 
since a too much<br> 
control throw may cause instability, e.g. Pilot Induced Oscillations 
(PIO).
<br><br>
Static stability is proportional to the stabiliser area and the tail 
moment. <br>
You get double static stability if you double the tail area or double 
the tail moment.<br>
Dynamic stability is also proportional to the stabiliser area but 
increases with the<br>

square of the tail moment, which means that you get four times the 
dynamic stability<br>
if you double the tail arm length.
<br><br>
However, making the tail arm longer or encreasing the stabiliser area 
will move<br>
the mass of the aircraft towards the rear, which may also mean the need 
to make<br>
the nose longer in order to minimize the weight required to balance the 
aircraft...
<br><br>
A totally stable aircraft will return, more or less immediately, to its 
trimmed state<br>
without pilot intervention.<br>
However, such an aircraft is rare and not much desirable. We usually 
want an <br>
aircraft just to be reasonably stable so it is easy to fly.<br>

If it is too stable, it tends to be sluggish in manoeuvring, exhibiting 
too slow <br>
response on the controls.
<br><br>
Too much instability is also an undesirable characteristic, except where
 an<br>
extremely manoeuvrable aircraft is needed and the instability can be 
continually<br>
corrected by on-board 'fly-by-wire' computers rather than the pilot, 
such as a<br>
supersonic air superiority fighter.
<br><br>
<b>Lateral stability</b> is achieved through dihedral, sweepback, keel 
effect and<br>
proper distribution of weight.<br>

The dihedral angle is the angle that each wing makes with the horizontal. If a <br> 
disturbance causes one wing to drop, the lower wing will receive 
more lift<br>
and the aircraft will roll back into the horizontal level.
<br><br>
A sweptback wing is one in which the leading edge slopes backward. <br>
When a disturbance causes an aircraft with sweepback to slip or drop a 
wing,<br>
the low wing presents its leading edge at an angle more perpendicular to
 the<br>
relative airflow. As a result, the low wing acquires more lift and 
rises, restoring<br> 
the aircraft to its original flight attitude.

<br><br>
The keel effect occurs with high wing aircraft. These are laterally 
stable simply<br>
because the wings are attached in a high position on the fuselage, 
making the<br>
fuselage behave like a keel. <br>
When the aircraft is disturbed and one wing dips, the fuselage weight 
acts like<br>
a pendulum returning the aircraft to the horizontal level.
<br><br>
The tail fin determines the <b>directional stability</b>.<br>
If a gust of wind strikes the aircraft from the right it will be in a 
slip and the fin<br>

will get an angle of attack causing the aircraft to yaw until the slip 
is eliminated. <br>
<br><br>
<img src="images/directional3.gif" border="0" height="243" width="272"><img src="images/directional4.gif" border="0" height="223" width="262">
<br><br>
<b>Longitudinal stability</b> depends on the location of the centre of 
gravity, the<br>
stabiliser area and how far the stabiliser is placed from the main wing.<br>
 
Most aircraft would be completely unstable without the horizontal 
stabiliser.
<br><br>
Non-symmetrical cambered airfoils have a higher lift coefficient, but 
they also <br>
have a negative pitching moment (Cm) tending to pitch nose-down, and 
thus <br>

being statically unstable, which requires the counter moment produced by
 the <br>
horizontal stabiliser to get adequate longitudinal stability.<br>
The stabiliser provides the same function in longitudinal stability as 
the fin does<br>
in directional stability.
<br><br>
Symmetrical (zero camber) airfoils have normally a zero pitching moment,<br>
 
resulting in neutral stability, which means the aircraft goes wherever 
you point it.<br>
Reflexed airfoils (with trailing edge bent up) have a positive pitching 
moment<br>
making them naturally stable, they are often used with flying wings 
(without the<br> 

horizontal stabiliser). 
<br><br>
It is of crucial importance that the aircraft's <b>Centre of Gravity 
(CG)</b> is located <br>
at the right point, so that a stable and controllable flight can be 
achieved.<br>
In order to achieve a good longitudinal stability, the CG should be 
ahead of the <br>
<b>Neutral Point (NP)</b>, which is the Aerodynamic Centre of the whole 
aircraft. <br>
NP is the position through which all the net lift increments act for a 
change in <br>
angle of attack.<br> 

The major contributors are the main wing, stabiliser surfaces and 
fuselage.
<br><br>
The bigger the stabiliser area in relationship to the wing area and the 
longer <br>
the tail moment arm relative to the wing chord, the farther aft the NP 
will be and <br>
the farther aft the CG may be, provided it's kept ahead of the NP for 
stability.
<br><br>
<img src="images/np.gif" border="0" height="144" width="393">
<br><br>
The angle of the fuselage to the direction of flight affects its drag, 
but has little<br>
effect on the pitch trim unless both the projected area of the fuselage 
and its<br>
angle to the direction of flight are quite large. 
<br><br>
A <b>tail-heavy</b> aircraft will be more unstable and susceptible to 
stall at low speed<br>

e. g. during the landing approach.<br> 
A <b>nose-heavy</b> aircraft will be more difficult to takeoff from the 
ground and to<br>
gain altitude and will tend to drop its nose when the throttle is 
reduced. It also<br>
requires higher speed in order to land safely.
<br><br>
The angle between the wing chord line and the stabiliser chord line is 
called <br>
the <b>Longitudinal Dihedral (LD)</b> or decalage.<br>

For a given centre of gravity, there is a LD angle that results in a 
certain <br>
trimmed flight speed and pitch attitude. <br>
If the LD angle is increased the plane will take on a more nose up pitch
 attitude,<br>
whereas with a decreased LD angle the plane will take on a more nose 
down<br>
pitch attitude. <br>
There is also the <b>Angle of Incidence</b>, which is the angle of a 
flying surface<br>
related to a common reference line drawn by the designer along the 
fuselage.<br>
The designer might want this reference line to be level when the plane 
is flying <br>

at level flight or when the fuselage is in it's lowest drag position.<br>
The purpose of the reference line is to make it easier to set up the 
relationships <br>
among the thrust, the wing and the stabiliser incidence angles.<br>
Thus, the Longitudinal Dihedral and the Angle of Incidence are 
interdependent. 
<br><br>
Longitudinal stability is also improved if the stabiliser is situated so
 that it lies<br>
outside the influence of the main wing downwash.<br>
Stabilisers are therefore often staggered and mounted at a different 
height in <br>  
order to improve their stabilising effectiveness.
<br><br>
It has been found both experimentally and theoretically that, if the 
aerodynamic <br>

force is applied at a location 1/4 from the leading edge of a 
rectangular wing <br>
at subsonic speed, the magnitude of the aerodynamic moment remains 
nearly <br>
constant even when the angle of attack changes.<br>
This location is called the wing's <b>Aerodynamic Centre AC</b>. <br>
(At supersonic speed, the aerodynamic centre is near 1/2 of the chord). 
<br><br>
<img src="images/aerodyncent.gif" border="0" height="271" width="347">
<br><br>
In order to obtain a good Longitudinal Stability the <b>Centre of 
Gravity CG</b><br>

should be close to the main wings' <b>Aerodynamic Centre AC</b>.<br>
For wings with other than rectangular form (such as triangular, 
trapezoidal, <br>
compound, etc.) we have to find the <b>Mean Aerodynamic Chord - MAC</b>,
 <br>
which is the average for the whole wing.<br>
The MAC calculation requires rather complicated mathematics, so a 
simpler<br> 
method called 'Geometric Mean Chord' GMC or 'Standard Mean Chord' SMC <br>
may be used as shown on the drawings below.<br>

MAC is only slightly bigger than GMC except for sharply tapered wings.<br>
Taper ratio = tip chord/root chord.
<br><br>
<img src="images/wing.gif" border="0" height="92" width="352"><br>
<img src="images/wing1.gif" border="0" height="230" width="362"><br>
To calculate MAC of a tapered wing, the following simplified equation<br>
may be used:<br>
MAC = root chord * 2/3 * ((1+T+T<sup>2</sup>)/(1+T))<br>
Where T is the wing's taper ratio.<br><br>
The MAC distance from the center line may be calculated as follows:<br>

distance = half span * (1+2*T)/(3+3*T)<br><br> 
<img src="images/wing2.gif" border="0" height="231" width="355"><br>
For a delta wing the <b>CG</b> should be located 10% ahead of the 
geometrically<br> 
calculated <b>AC</b> point as shown above.<br>
<img src="images/wing4.gif" border="0" height="137" width="375"><br>
The MAC of an elliptical wing is 85% of the root chord and is located at
 42.4% of<br> 
the half wingspan from the root chord.<br> 

Elliptical wing's area = pi * wingspan * root chord/4  
<br><br>
The <b>AC</b> location for biplanes with positive stagger (top wing 
ahead of the bottom<br>
wing), is found according to the drawing below.<br>
<img src="images/bipe_cg.gif" border="0" height="341" width="448"><br>
For conventional designs (with main wing and horizontal stab) the <b>CG</b>
 location <br>
range is usually between 28% and 33% from the leading edge of the main<br>

wing's MAC, which means between about 5% and 15% ahead of the aircraft's
 <br>
Neutral Point <b>NP</b>.<br>
This is called the <b>Static Margin</b>, which is expressed as a 
percentage of MAC.<br> 
When the static margin is zero (CG coincident with NP) the aircraft is 
considered<br>
"neutrally stable".<br>
However, for conventional designs the static margin should be between 5%
 and <br>
15% of the MAC ahead of the NP.

<br><br>
The CG location as described above is pretty close to the wing's 
Aerodynamic<br>
Center AC because the lift due to the horizontal stab has only a 
slightly effect on<br>
the conventional R/C models.
<br><br>
However, those figures may vary with other designs, as the NP location 
depends <br>
on the size of the main wing vs. the stab size and the distance between 
the main <br>
wing's AC and the stab's AC.<br>
The simplest way of locating the aircraft's NP is by using the areas of 
the two <br>
horizontal lifting surfaces (main wing and stab) and locate the NP 
proportionately<br>

along the distance between the main wing's AC point and the stab's AC 
point.<br>
For example, the NP distance to the main wing's AC point would be:<br>
D = L <b>·</b> (stab area) / (main wing area + stab area) as shown on 
the picture below:<br>
<img src="images/np_location.gif" border="0" height="297" width="408">
<br><br>
There are other factors, however, that make the simple formula above 
inaccurate.<br> 
In case the two wings have different aspect ratios (different 
dCL/d-alpha) the NP<br>
will be closer to the one that has higher aspect ratio.<br>

Also, since the stab operates in disturbed air, the NP will be more 
forward than<br>
the simple formula predicts.
<br><br>
The figure below shows a somewhat more complex formula to locate the NP 
but<br>
would give a more accurate result using the so called Tail Volume Ratio,
 <b>Vbar</b>.<br>
This formula gives the NP position as a percentage (%) of the wing's MAC
 aft of <br>
the wing's AC point.
